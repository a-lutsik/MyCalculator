package calcOperations;

public class CalcOperations {

    public static Double add(double chislo_1, double chislo_2) {
        return chislo_1 + chislo_2;
    }

    public static Double substruct(double chislo_1, double chislo_2) {
        return chislo_1 - chislo_2;
    }

    public static Double increase(double chislo_1, double chislo_2) {
        return chislo_1 * chislo_2;
    }

    public static Double division(double chislo_1, double chislo_2) {
        return chislo_1 / chislo_2;
    }
}
